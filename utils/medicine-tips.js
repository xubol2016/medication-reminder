/**
 * 查询药物提示信息
 * @param {string} medicineName 药物名称
 * @returns {object} 提示信息（始终返回有效对象）
 */
function queryTip(medicineName) {
  if (!medicineName) return GENERIC_TIP
  var name = medicineName.trim()

  // 1. 精确匹配
  if (EXACT_TIPS[name]) {
    return EXACT_TIPS[name]
  }

  // 2. 包含匹配（药名包含或被包含）
  var keys = Object.keys(EXACT_TIPS)
  for (var i = 0; i < keys.length; i++) {
    var key = keys[i]
    if (name.indexOf(key) >= 0 || key.indexOf(name) >= 0) {
      return EXACT_TIPS[key]
    }
  }

  // 3. 关键字分类匹配
  for (var j = 0; j < KEYWORD_TIPS.length; j++) {
    var item = KEYWORD_TIPS[j]
    for (var k = 0; k < item.keywords.length; k++) {
      if (name.indexOf(item.keywords[k]) >= 0) {
        return item.tip
      }
    }
  }

  // 4. 返回通用提示
  return GENERIC_TIP
}

module.exports = { queryTip: queryTip }
